    private void getRandomNums() {
        // get random numbers between 1-9
        // put them in random places on the board
        // set them into the board array to house them
        // make those textFields in the GUI uneditable

        // Get 30 random text fields to fill. 
        // fill each with a random number 1-9
        // check if the solution is correct so far
        // If not it calls recursion to repeat the method and do it over again
        
        
        // THIS GETS RANDOM TEXT FIELDS TO FILL --------------------------------
        // ---------------------------------------------------------------------
        // textFieldIndexs is all the available text fields, this is used to not
        // repeat any text fields
        
        ArrayList<Integer> textFieldIndexs = new ArrayList<Integer>();

        for (int y = 0; y < textFields.length; y++) {
            textFieldIndexs.add(y);
        }

        // textFieldRandoms hold the 30 random numbers that correlate to the
        // random textFields chosen
        ArrayList<Integer> textFieldRandoms = new ArrayList<Integer>();

        Random rand = new Random();
        int numOfThingsNeeded = 30;

        for (int i = 0; i < numOfThingsNeeded; i++) {
            // Get a random index between 0 and the length of the number of 
            // text field indexs I still have
            int randIndex = rand.nextInt(textFieldIndexs.size());

            // Add the number from that random index to a new array holding
            // the randomly selected text fields I need to fill
            textFieldRandoms.add(textFieldIndexs.get(randIndex));

            //Remove that index from the list of indexs to not repeat a number
            textFieldIndexs.remove(randIndex);
        }
        // ---------------------------------------------------------------------
        // ---------------------------------------------------------------------

        
        // FILLS EACH RANDOM TEXT FIELD WITH A NUMBER BETWEEN 1-9 ==============
        // =====================================================================
        // uses textFieldRandoms as the indexs for the random text fields between 0-80
        
        while (textFieldRandoms.size() != 0) {
            
            int randField = textFieldRandoms.get(0);
            
            boolean goNextRand = false;
            
            while(goNextRand == false){
                
                int randNum = rand.nextInt(10);
                
                while (randNum == 0) {
                    randNum = rand.nextInt(10);
                }
                
                textFields[randField].setText("" + randNum);
                
                setArrays();
                
                if(isCorrect()){
                    goNextRand = true;
                }
            }
            
            
            textFields[randField].setEditable(false);
            
            textFieldRandoms.remove(0);
        }

        // =====================================================================
        // =====================================================================
        
        setArrays();

    }

    private boolean isCorrect() {
        // checks to see if the solution so far is correct
        // so if there is any repeating number in the same row, collumn, or box

        // Check each row and Column and Box------------------------------------
        //----------------------------------------------------------------------
        Queue<Integer> rows = new LinkedList<Integer>();
        ArrayList<Integer> checkRow = new ArrayList<Integer>();

        Queue<Integer> columns = new LinkedList<Integer>();
        ArrayList<Integer> checkCol = new ArrayList<Integer>();

        Queue<Integer> box = new LinkedList<Integer>();
        ArrayList<Integer> checkBox = new ArrayList<Integer>();

        int atBoxNum = 1;

        for (int r = 0; r < board.length; r++) {
            for (int c = 0; c < board[r].length; c++) {
                int rowNumber = board[r][c];
                int colNumber = board[c][r];

                rows.add(rowNumber);
                checkRow.add(rowNumber);

                columns.add(colNumber);
                checkCol.add(colNumber);

                //System.out.println("The Box Num is at: " + atBoxNum);
                // Row 1
                if (atBoxNum == 1 && r == 0 && c > -1 && c < 3) {
                    box.add(board[0][c]);
                    checkBox.add(board[0][c]);

                    box.add(board[1][c]);
                    checkBox.add(board[1][c]);

                    box.add(board[2][c]);
                    checkBox.add(board[2][c]);

                }
                if (atBoxNum == 2 && r == 1 && c > 2 && c < 6) {
                    box.add(board[0][c]);
                    checkBox.add(board[0][c]);

                    box.add(board[1][c]);
                    checkBox.add(board[1][c]);

                    box.add(board[2][c]);
                    checkBox.add(board[2][c]);
                }
                if (atBoxNum == 3 && r == 2 && c > 5 && c < 9) {
                    box.add(board[0][c]);
                    checkBox.add(board[0][c]);

                    box.add(board[1][c]);
                    checkBox.add(board[1][c]);

                    box.add(board[2][c]);
                    checkBox.add(board[2][c]);
                }

                // Row 2
                if (atBoxNum == 4 && r == 3 && c > -1 && c < 3) {
                    box.add(board[3][c]);
                    checkBox.add(board[3][c]);

                    box.add(board[4][c]);
                    checkBox.add(board[4][c]);

                    box.add(board[5][c]);
                    checkBox.add(board[5][c]);
                }
                if (atBoxNum == 5 && r == 4 && c > 2 && c < 6) {
                    box.add(board[3][c]);
                    checkBox.add(board[3][c]);

                    box.add(board[4][c]);
                    checkBox.add(board[4][c]);

                    box.add(board[5][c]);
                    checkBox.add(board[5][c]);
                }
                if (atBoxNum == 6 && r == 5 && c > 5 && c < 9) {
                    box.add(board[3][c]);
                    checkBox.add(board[3][c]);

                    box.add(board[4][c]);
                    checkBox.add(board[4][c]);

                    box.add(board[5][c]);
                    checkBox.add(board[5][c]);
                }

                // Row 3
                if (atBoxNum == 7 && r == 6 && c > -1 && c < 3) {
                    box.add(board[6][c]);
                    checkBox.add(board[6][c]);

                    box.add(board[7][c]);
                    checkBox.add(board[7][c]);

                    box.add(board[8][c]);
                    checkBox.add(board[8][c]);
                }
                if (atBoxNum == 8 && r == 7 && c > 2 && c < 6) {
                    box.add(board[6][c]);
                    checkBox.add(board[6][c]);

                    box.add(board[7][c]);
                    checkBox.add(board[7][c]);

                    box.add(board[8][c]);
                    checkBox.add(board[8][c]);
                }
                if (atBoxNum == 9 && r == 8 && c > 5 && c < 9) {
                    box.add(board[6][c]);
                    checkBox.add(board[6][c]);

                    box.add(board[7][c]);
                    checkBox.add(board[7][c]);

                    box.add(board[8][c]);
                    checkBox.add(board[8][c]);
                }
            }

            //While for ROWS
            while (!rows.isEmpty()) {
                int rowNum = rows.remove();
                int rowNumCnt = 0;

                //System.out.println("rowNum = " + rowNum);
                for (int i = 0; i < checkRow.size(); i++) {
                    if (checkRow.get(i) == rowNum && rowNum != 0) {
                        //System.out.println("rowCnt ++ " + "rowCheck = " + checkRow.get(i));
                        rowNumCnt++;
                    }
                }

                //System.out.println("rowCnt = " + rowNumCnt);
                if (rowNumCnt > 1) {
                    return false;
                }
            }

            //While for COLUMNS
            while (!columns.isEmpty()) {
                int colNum = columns.remove();
                int colNumCnt = 0;

                //System.out.println("colNum = " + colNum);
                for (int x = 0; x < checkCol.size(); x++) {
                    if (checkCol.get(x) == colNum && colNum != 0) {
                        //System.out.println("colCnt ++ " + "colCheck = " + checkCol.get(x));
                        colNumCnt++;
                    }
                }

                //System.out.println("colCnt = " + colNumCnt);
                if (colNumCnt > 1) {
                    return false;
                }
            }

            //While for BOXS
            while (!box.isEmpty()) {
                //System.out.println("\nCHECKING BIG BOX " + atBoxNum);

                int boxNum = box.remove();
                int boxNumCnt = 0;

                //System.out.println("boxNum = " + boxNum);
                for (int x = 0; x < checkBox.size(); x++) {
                    if (checkBox.get(x) == boxNum && boxNum != 0) {
                        //System.out.println("boxCnt ++ " + "boxCheck = " + checkBox.get(x));
                        boxNumCnt++;
                    }
                }

                //System.out.println("boxCnt = " + boxNumCnt + " at bocNum " + boxNum);
                if (boxNumCnt > 1) {
                    return false;
                }
            }

            //System.out.println("");
            while (checkRow.size() != 0) {
                checkRow.remove(0);
            }
            while (checkCol.size() != 0) {
                checkCol.remove(0);
            }
            while (checkBox.size() != 0) {
                checkBox.remove(0);
            }

            atBoxNum++;
        }

        return true;
    }

    private void setBoard() {
        // Takes the 2d array and sets the text fields in the GUI to
        // the numbers in it

        int fieldCnt = 0;

        for (int r = 0; r < board.length; r++) {
            for (int c = 0; c < board[r].length; c++) {
                int boardNum = board[r][c];

                if (boardNum == 0) {
                    textFields[fieldCnt].setText("");
                } else {
                    textFields[fieldCnt].setText(boardNum + "");
                }

                fieldCnt++;
            }
        }

    }

    private void setArrays() {
        // Takes the text from the text fields in the GUI
        // and sets those values in the 2d array

        int fieldCnt = 0;

        for (int r = 0; r < board.length; r++) {

            for (int c = 0; c < board.length; c++) {

                String fieldText = textFields[fieldCnt].getText();

                if (fieldText.equals("")) {
                    fieldText = "" + 0;
                }

                board[r][c] = Integer.parseInt(fieldText);

                fieldCnt++;
            }
        }

    }

    private boolean isFull() {
        // Checks to see if the board is completely filled out yet

        boolean full = true;

        for (int r = 0; r < board.length; r++) {
            for (int c = 0; c < board[r].length; c++) {
                if (board[r][c] < 1 || board[r][c] > 9) {
                    full = false;
                }
            }
        }

        return full;
    }

    private boolean inBounds(String input) {
        // called in every action listener for the textFields
        // Checks to see if the number the user has input is between 1-9

        if (Pattern.matches("[1-9]+", input) && Integer.parseInt(input) >= 1 && Integer.parseInt(input) <= 9) {
            return true;
        }

        return false;
    }

