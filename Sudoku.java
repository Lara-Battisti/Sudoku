    private void button_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_checkActionPerformed
        // CHECK BUTTON
        // Make the check to be able to see if anything is wrong or not
        // Depending on the solution of the user so far it sends a diff message
        // incomplete & correct: "Correct So Far!"
        // incomplete & wrong: "There's a wrong answer somewhere :("
        // complete & correct: "You Won! :D"
        // complete & wrong: "You Lost :(      Try Again?"

        boolean canSet = true;

        for (JTextField field : textFields) {
            if (!inBounds(field.getText()) && !field.getText().equals("")) {
                canSet = false;
            }
        }

        if (canSet) {
            setArrays();

            if (isCorrect() && !isFull()) {
                sendMessage("Correct So Far!", "Check");
            } else if (isCorrect() && isFull()) {
                sendMessage("Click Restart to play again.", "YOU WON!");
            } else if (!isCorrect() && !isFull()) {
                sendMessage("There's a wrong answer somewhere.", "Check");
            } else if (!isCorrect() && isFull()) {
                sendMessage("Fix your mistakes or Restart.", "YOU LOST");
            }
        } else {
            sendMessage("Please Review Your Answers", "An Input Is Not Accepted!!!");
        }


    }//GEN-LAST:event_button_checkActionPerformed

