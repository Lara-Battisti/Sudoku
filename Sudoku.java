    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        // SAVE BUTTON
        // Saves the progress that the player has made 
        // (saves the 2D array with the board)
        
        setArrays();
        
        // If there is no file available then it creates a new file to export
        if (currentFile == null){
            JFileChooser jfc = new JFileChooser();
            
            if (jfc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION){
                currentFile = jfc.getSelectedFile();
                
                try {

                    FileOutputStream fos = new FileOutputStream(currentFile);
                    ObjectOutputStream oos = new ObjectOutputStream(fos);

                    oos.writeObject(new SModel(board));
                    oos.close();

                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Sudoku.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Sudoku.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            
        }
        
        // If there is a file then it overrides it
        if (currentFile != null){
            
            try {

                FileOutputStream fos = new FileOutputStream(currentFile);
                ObjectOutputStream oos = new ObjectOutputStream(fos);

                oos.writeObject(new SModel(board));
                oos.close();

            } catch (FileNotFoundException ex) {
                Logger.getLogger(Sudoku.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Sudoku.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
    }//GEN-LAST:event_button_saveActionPerformed

